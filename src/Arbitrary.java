public class Arbitrary {
    public static void main(String[] args) {
        int[] myarray;
        myarray = new int[4];
        myarray[0] = 10;
        System.out.println("myarray[0] = " + myarray[0]);
        System.out.println("myarray[1] = " + myarray[1]);
        int[] wholeArray = {1, 3, 5, 7};
        System.out.println("wholeArray[0] = " + wholeArray[0]);
        System.out.println("wholeArray[1] = " + wholeArray[1]);
        System.out.println("wholeArray[2] = " + wholeArray[2]);
        System.out.println("wholeArray[3] = " + wholeArray[3]);
    }
    }
